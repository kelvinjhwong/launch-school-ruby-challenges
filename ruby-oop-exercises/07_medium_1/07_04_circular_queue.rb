class CircularQueue
  def initialize(buffer_size)
    @buffer_size = buffer_size
    @objects = []
  end
  
  def enqueue(object)
    objects.shift if objects.size == buffer_size
    objects << object
  end
  
  def dequeue
    objects.shift
  end
  
  private
  
  attr_reader :buffer_size
  attr_accessor :objects
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
